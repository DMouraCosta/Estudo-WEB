// Calculadora Simples:

// Crie funções para realizar operações básicas de uma calculadora:

// Função soma(a, b) que retorna a soma de a e b.
// Função subtracao(a, b) que retorna a subtração de a por b.
// Função multiplicacao(a, b) que retorna o produto de a e b.
// Função divisao(a, b) que retorna o resultado da divisão de a por b.

// Teste as funções com diferentes valores e imprima os resultados no console.

function soma(a, b){
    return a+b;
};

function subtracao(a,b){
    return a-b;
};

function multiplicacao(a,b){
    return a*b;
};

function divisao(a,b){
    return a/b;
};

console.log(soma(5,3));
console.log(subtracao(8,3));
console.log(multiplicacao(4,2));
console.log(divisao(10,4));
